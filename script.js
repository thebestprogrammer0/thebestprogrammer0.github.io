function reset() {
    currentIndex = 0;
    document.getElementById ("songName").innerHTML = "";
    document.getElementById ("isPaused").style.display = 'inline-block';
    document.getElementById ("isPlaying").style.display = 'none';
    document.getElementsByClassName("musicList")[0].innerHTML = "";
    document.getElementsByClassName("musicList")[1].innerHTML = "";
    document.getElementsByClassName("musicList")[2].innerHTML = "";

    isPaused = true;
}

