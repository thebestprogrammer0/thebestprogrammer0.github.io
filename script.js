let nameArray;
let musicList;

let indexDiv;
let nameDiv;
let authorDiv;
let timeDiv;
let newButton;
let currentIndex = 0;

let isPaused = true;

function setMusic() {
    if (window.getComputedStyle (document.getElementById ("mafia1")).display !== 'none') {
        linkArray = linkArray1;
        nameArray = nameArray1;
        musicList = document.getElementsByClassName ("musicList")[0];
    } else if (window.getComputedStyle (document.getElementById ("mafia2")).display !== 'none') {
        linkArray = linkArray2;
        nameArray = nameArray2;
        musicList = document.getElementsByClassName ("musicList")[1];
    } else if (window.getComputedStyle (document.getElementById ("mafia3")).display !== 'none') {
        linkArray = linkArray3;
        nameArray = nameArray3;
        musicList = document.getElementsByClassName ("musicList")[2];
    }

    for (let i = 0; i < linkArray.length; i++) {
        newButton = document.createElement ("button");
        newButton.style.display = 'flex';
        newButton.style.alignItems = 'center';

        if(window.matchMedia("(max-width: 430px)").matches) {
            newButton.style.height = "100px";
        }
        else {
            newButton.style.height = "55px";
        }

        newButton.name = linkArray[i];
        newButton.id = `${i}`;

        indexDiv = document.createElement ("div");
        indexDiv.classList.add("indexDiv");
        indexDiv.style.width = "10px";
        indexDiv.innerHTML = i + 1;

        let string = nameArray[i];

        nameDiv = document.createElement ("div");
        nameDiv.style.width = "450px";
        nameDiv.style.textAlign = 'left';
        nameDiv.style.paddingLeft = '50px';
        nameDiv.innerHTML = string.split(" - ")[1];

        authorDiv = document.createElement ("div");
        authorDiv.style.width = "360px";
        authorDiv.style.textAlign = 'left';
        authorDiv.style.paddingLeft = '50px';
        authorDiv.innerHTML = string.split(" - ")[0];

        timeDiv = document.createElement ("div");
        timeDiv.style.width = '60px';

        timeDiv.innerHTML = "mp3";

        newButton.appendChild (indexDiv);
        newButton.appendChild (nameDiv);
        newButton.appendChild (authorDiv);
        newButton.appendChild (timeDiv);
        musicList.appendChild (newButton);

        newButton.onclick = function () {
            document.getElementById(`${currentIndex}`).style.background = 'none';
            currentIndex = this.id;
            document.title = `${nameArray[currentIndex]}`;

            document.getElementById(`${currentIndex}`).style.backgroundColor = "rgba(11, 21, 33, 0.7)";
            document.getElementById ("songName").innerHTML = `${nameArray[currentIndex]}`;
            document.getElementById ("music").src = this.name;
            document.getElementById ("isPaused").style.display = 'none';
            document.getElementById ("isPlaying").style.display = 'inline-block';
            document.getElementById ("music").play();
            isPaused = false;
        };
    }

    document.getElementById("mainImagesContainer").style.display = 'none';
    document.getElementById ("musicControl").style.display = 'flex';
    document.getElementById(`${currentIndex}`).style.backgroundColor = "rgba(11, 21, 33, 0.7)";
    document.getElementById ("music").src = `${linkArray[currentIndex]}`;
    document.getElementById ("songName").innerHTML = `${nameArray[currentIndex].split(" - ")[1]}`;
    document.title = `${nameArray[currentIndex]}`;
}

function pausePlayMusic() {
    if (isPaused === true) {
        document.getElementById ("isPaused").style.display = 'none';
        document.getElementById ("isPlaying").style.display = 'inline-block';
        document.getElementById ("music").play();

        isPaused = false;
    } else {
        document.getElementById ("isPaused").style.display = 'inline-block';
        document.getElementById ("isPlaying").style.display = 'none';
        document.getElementById ("music").pause();

        isPaused = true;
    }
}

function nextMusic() {
    document.getElementById(`${currentIndex}`).style.background = 'none';
    if (currentIndex < (linkArray.length - 1)) {
        ++currentIndex;
    } else {
        currentIndex = 0;
    }

    document.getElementById(`${currentIndex}`).style.backgroundColor = "rgba(11, 21, 33, 0.7)";
    document.getElementById ("music").src = `${linkArray[currentIndex]}`;
    document.getElementById ("songName").innerHTML = `${nameArray[currentIndex].split(" - ")[1]}`;
    document.title = `${nameArray[currentIndex]}`;

    if (isPaused === false) {
        document.getElementById ("music").play ();
    }
}

function prevMusic() {
    document.getElementById(`${currentIndex}`).style.background = 'none';
    if (currentIndex > 0) {
        --currentIndex;
    } else {
        currentIndex = linkArray.length - 1;
    }

    document.getElementById(`${currentIndex}`).style.backgroundColor = "rgba(11, 21, 33, 0.7)";
    document.getElementById ("music").src = `${linkArray[currentIndex]}`;
    document.getElementById ("songName").innerHTML = `${nameArray[currentIndex].split(" - ")[1]}`;
    document.title = `${nameArray[currentIndex]}`;

    if (isPaused === false) {
        document.getElementById ("music").play ();
    }
}

document.getElementById ("music").onended = () => {
    if(isPaused === false) {
        setTimeout(function() {
            if(document.getElementById("music").ended === true && isPaused === false) {
                document.getElementById (`${currentIndex}`).style.background = 'none';
                if (currentIndex < (linkArray.length - 1)) {
                    ++currentIndex;
                } else {
                    currentIndex = 0;
                }

                document.getElementById (`${currentIndex}`).style.backgroundColor = "rgba(11, 21, 33, 0.7)";
                document.getElementById ("music").src = `${linkArray[currentIndex]}`;
                document.getElementById ("songName").innerHTML = `${nameArray[currentIndex].split(" - ")[1]}`;
                document.title = `${nameArray[currentIndex]}`;
                document.getElementById ("music").play ();
            }
        },2000)
    }
};

